const a = 7 //= quer dizer que estamos atribuindo o valor 7 a variavel contante a

//Atribuições combinadas com outros operadores 
let b = 3

//Atribuição aditiva
b += a //b = b + a; Acrescentando o valor a, a variavel b
console.log(b)

//Atribuição subtrativa
b -= 4 // b = b - 4; Subtraindo 4 do valor de b
console.log(b)

//Atribuição multiplicativa
b *= 2 // b = b * 2
console.log(b)

//Atribuição divisiva
b /= 2 // b = b / 2
console.log(b)

//Atribuição modular
b %= 2 // b = b % 2; Resto da operacao, que é 6 resto 0; 
console.log(b)
